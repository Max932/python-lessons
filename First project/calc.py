command = input ("Введите команду: ")
num1 = int (input ("Введите число 1: "))
num2 = int (input ("Введите число 2: "))

if command == "+" :
    print (num1 + num2)
elif command == "*" :
    print (num1 * num2)
elif command == "/" :
    print (num1 / num2)
if command == "-" :
    print (num1 - num2)
if command == "more" :
    print (num1 + num2)
